
#include "main.h"
#include "ADP.h"

SPI_HandleTypeDef hspi1;


void SystemClock_Config(void);

static void MX_GPIO_Init(void);

static void MX_SPI1_Init(void);

union FifoData fifoData = {0, 0};
uint64_t maxValue = 0;

//----------------------------------------------------------------------------------------------------------------------

int main(void) {

    HAL_Init();

    SystemClock_Config();

    MX_GPIO_Init();
    MX_SPI1_Init();

    __HAL_RCC_GPIOA_CLK_ENABLE();


    struct CsPin csPin = {GPIOA, GPIO_PIN_15};

    if (smokeSensorADPD188Init(hspi1, csPin) == HAL_OK) {
        HAL_Delay(1);
    } else {
        HAL_Delay(2);
    }
    while (1) {

        fifoData = readData(hspi1, csPin);
        if (fifoData.raw != 0x00) {
            if (fifoData.bytes.SLOTA + fifoData.bytes.SLOTB > 2120) {
                HAL_Delay(1);
            }
            if (fifoData.bytes.SLOTA + fifoData.bytes.SLOTB > maxValue) {
                maxValue = fifoData.bytes.SLOTA + fifoData.bytes.SLOTB;
            }

        }
        HAL_Delay(1);

    }

}

